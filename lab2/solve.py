def thomas_algorithm(a, b, c, d):
    n = len(b)

    p = [0.0] * n
    q = [0.0] * n

    denom = b[0]
    p[0] = -c[0] / denom if n > 1 else 0.0
    q[0] = d[0] / denom

    for i in range(1, n):
        denom = b[i] + a[i] * p[i - 1]
        p[i] = -c[i] / denom if i < n - 1 else 0.0
        q[i] = (d[i] - a[i] * q[i - 1]) / denom

    x = [0.0] * n
    x[n - 1] = q[n - 1]
    for i in range(n - 2, -1, -1):
        x[i] = p[i] * x[i + 1] + q[i]

    return x, p, q


def tridiagonal_mat_vec(a, b, c, x):
    n = len(b)
    res = [0.0] * n
    for i in range(n):
        s = b[i] * x[i]
        if i > 0:
            s += a[i] * x[i - 1]
        if i < n - 1:
            s += c[i] * x[i + 1]
        res[i] = s
    return res


def print_vector(name, v, digits=6):
    print(name)
    print("[" + ", ".join(f"{x:.{digits}f}" for x in v) + "]")


