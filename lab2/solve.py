# Подробно и по-человечески про метод прогонки (на наших данных):
#
# Что у нас на входе:
# a = [0, -8, -5, 1, -4]      # нижняя диагональ
# b = [14, 14, -17, 5, -10]   # главная диагональ
# c = [9, 6, 8, -2, 0]        # верхняя диагональ
# d = [125, -56, 144, 36, 70] # правая часть
#
# Идея метода:
# 1) Прямой ход: считаем P и Q слева направо.
# 2) Обратный ход: считаем x справа налево.
#
# Формулы прямого хода:
# P[0] = -c[0] / b[0]
# Q[0] =  d[0] / b[0]
# P[i] = -c[i] / (b[i] + a[i]*P[i-1]), i = 1..n-2
# Q[i] = (d[i] - a[i]*Q[i-1]) / (b[i] + a[i]*P[i-1]), i = 1..n-1
# Для последней строки P[n-1] = 0 (потому что c[n-1] = 0).
#
# Считаем числа для нашей системы:
# P1 = -9/14 = -0.642857
# Q1 = 125/14 = 8.928571
#
# P2 = -6 / (14 + (-8)*P1)
#    = -6 / (14 + 5.142856) = -0.313433
# Q2 = (-56 - (-8)*Q1) / (14 + (-8)*P1)
#    = 15.428568 / 19.142856 = 0.805970
#
# P3 = -8 / (-17 + (-5)*P2)
#    = -8 / (-17 + 1.567165) = 0.518375
# Q3 = (144 - (-5)*Q2) / (-17 + (-5)*P2)
#    = 148.029850 / (-15.432835) = -9.591876
#
# P4 = -(-2) / (5 + 1*P3)
#    = 2 / 5.518375 = 0.362426
# Q4 = (36 - 1*Q3) / (5 + 1*P3)
#    = 45.591876 / 5.518375 = 8.261830
#
# P5 = 0
# Q5 = (70 - (-4)*Q4) / (-10 + (-4)*P4)
#    = 103.047320 / (-11.449704) = -9.000000
#
# Обратный ход (идем с конца):
# x5 = Q5 = -9
# x4 = P4*x5 + Q4 = 0.362426*(-9) + 8.261830 = 5
# x3 = P3*x4 + Q3 = 0.518375*5 + (-9.591876) = -7
# x2 = P2*x3 + Q2 = (-0.313433)*(-7) + 0.805970 = 3
# x1 = P1*x2 + Q1 = (-0.642857)*3 + 8.928571 = 7
#
# Итого решение:
# x = [7, 3, -7, 5, -9]
#
